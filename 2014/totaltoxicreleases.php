        <h1>Total Toxic Releases</h1>
        <h2>Units: Lbs</h2>
        <br>
        <br> To account for the amount of chemicals released into the
        environment, the Total Toxic Releases variable was selected
        to evaluate facilities because specific toxic releases can have
        significant environmental and public health effects,
        especially if they are released in extremely large quantities. This
        variable includes on- and off-site releases from
        production to air, water, land, Class I underground injection wells,
        Resource Conservation and Recovery Act (RCRA)
        Subtitle C landfills, and other landfills. It excludes toxic
        releases due to catastrophic, one-time events so that
        toxic releases related to facility production processes are only
        considered.
        <br>
        <br> More information on this variable and how it was used to
        calculate the
        <a href="eismethodology.php" target="_blank">Cal EcoMaps
          Environmental Impact Score</a> can be found in our
        <a href="Report.php" target="_blank">report</a>.
        <br>
        <br>Database:
        <a href="https://www2.epa.gov/toxics-release-inventory-tri-program/trinet"
          target="_blank">TRI.NET</a>
