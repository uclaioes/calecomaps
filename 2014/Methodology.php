        <h1>Scope of the Project</h1>
        Cal EcoMaps and the Cal EcoMaps EIS are based on 2012 (most recently
        updated) data from TRI, ReferenceUSA, Hoovers, and Orbis.
        Across four industries, 172 facilities received Cal EcoMaps EIS. Our
        analysis focuses on these four top emitting
        industries in Los Angeles County because they cumulatively account
        for nearly 7 million pounds, or 89% of the countyâ€™s
        2012 total toxic releases. The top four industries are: Primary
        Metals (38%), Petroleum (31%), Fabricated Metals
        (15%), and Chemicals (5%).
