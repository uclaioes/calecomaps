			<h1>Regional Contribution to Lifetime Cancer Risk from Air Emissions </h1>
			<h2>Units: Number of cancers in one million people exposed in the L.A.
				Basin over 70 years of exposure due to toxic air emissions by a facility
			</h2> <br><br>
			Because Toxicity of Total Releases is unitless measure, we calculated the
			facility Regional Contribution to Lifetime Cancer Risk to provide a more
			concrete measure of health-related impacts. Air releases were only
			considered because inhalation rates could be determined by age and sex to
			calculate the number of cancers in one million people (Exposure Factors
			Handbook, US EPA). Results for this variable are screening-level estimates
			and suggest that a particular facility may contribute approximately some
			number of cancers in a million to the overall lifetime risk in the Los
			Angeles Basin. This estimate does not constitute a risk assessment and
			should not be used to draw conclusions about individual risk.
			<br><br>
			More information on this variable and how it was used to calculate the <a
				href="eismethodology.php" target="_blank">Cal EcoMaps Environmental
				Impact Score</a> can be found in our <a href="Report.php"
				target="_blank">report</a>.
